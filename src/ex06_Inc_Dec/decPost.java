package ex06_Inc_Dec;

public class decPost {
    public static void main(String[] args) {
        int a =10;
        int result = a--;
        System.out.println(result);
        System.out.println(a);

        int b = 10;
        int res = --b;
        System.out.println(res);
        System.out.println(b);
    }
}
